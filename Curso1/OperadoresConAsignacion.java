
package Curso1;

public class OperadoresConAsignacion {
    
    public static void main( String[] args){
        int numero=10; 
        // quiero que el valor de numero cambie. 
        numero= numero+ 5 ; // esta es una forma de cambiar el valor, pero es muy tedioso. 
        
        // mejor intenta hacer esto, es igual que el de arriba. 
        numero +=5; 
        
        
        // tambien sirve para la resta. 
        numero -= 5; 
        
        // practicamente sirve para todos los operadores aritmeticos. 
        
        
        System.out.println(numero);
    
    }
    
}
